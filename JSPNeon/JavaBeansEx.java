package JSPNeon;
public class JavaBeansEx {
    private String name;
    private int age;

    public JavaBeansEx() {
        // Default constructor
    }

    // Getter and Setter methods for name and age properties

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}

